"""
Bulk migration workflow: clone repos, scan, track status, and send PRs.

Subcommands:
    prepare  - Clone flagged repos, create branches, scan, produce report
    status   - Show migration status of all prepared repos
    send-prs - Create PRs for migrated repos (all, selected, or interactive)

Prerequisites:
    gh CLI installed and authenticated (gh auth login)

Usage:
    python tools/bulk_migrate.py prepare --org YOUR_ORG
    python tools/bulk_migrate.py prepare --org YOUR_ORG --language python --workdir /tmp/migrations
    python tools/bulk_migrate.py status --workdir /tmp/migrations
    python tools/bulk_migrate.py send-prs --workdir /tmp/migrations
    python tools/bulk_migrate.py send-prs --workdir /tmp/migrations --repos repo1,repo2
    python tools/bulk_migrate.py send-prs --workdir /tmp/migrations --exclude repo3
"""

import argparse
import json
import os
import subprocess
import sys
import time
from pathlib import Path

BRANCH_NAME = "azure-openai-to-responses-api"
PR_TITLE = "Migrate from Chat Completions to Responses API"
PR_BODY = """## What this PR does

Migrates this app from Azure OpenAI Chat Completions to the Responses API.

### Changes
- `AzureOpenAI`/`AsyncAzureOpenAI` → `OpenAI`/`AsyncOpenAI` with `base_url`
- `chat.completions.create(messages=...)` → `responses.create(input=...)`
- `choices[0].message.content` → `resp.output_text`
- Test infrastructure updated (mocks, snapshots, assertions)
- Removed `api_version` / `AZURE_OPENAI_API_VERSION` from env vars and infra

### Why
GPT-5 and newer models require the Responses API. The `/openai/v1/` endpoint is stable and requires no `api_version` management.

### How to verify
```bash
pip install openai>=1.108.1 azure-identity
pytest
```

---
*Generated by [azure-openai-to-responses](https://github.com/YOUR_ORG/azure-openai-to-responses)*
"""

SEARCH_PATTERNS = [
    "chat.completions.create",
    "AzureOpenAI(",
    "AsyncAzureOpenAI(",
    "choices[0].message.content",
]
SEARCH_RATE_LIMIT_PAUSE = 7


def gh_run(args: list[str], cwd: str | Path | None = None) -> subprocess.CompletedProcess:
    """Run a gh CLI command. Returns CompletedProcess."""
    return subprocess.run(
        ["gh"] + args,
        capture_output=True, text=True, timeout=120, cwd=cwd,
    )


def git_run(args: list[str], cwd: str | Path) -> subprocess.CompletedProcess:
    """Run a git command in a specific directory."""
    return subprocess.run(
        ["git"] + args,
        capture_output=True, text=True, timeout=60, cwd=cwd,
    )


def discover_repos(org: str, language: str | None) -> list[str]:
    """Find repos in an org that have legacy patterns. Returns list of full repo names."""
    repos: set[str] = set()

    for pattern in SEARCH_PATTERNS:
        cmd = ["search", "code", "--owner", org, pattern, "--json", "repository", "--limit", "100"]
        if language:
            cmd.extend(["--language", language])

        result = gh_run(cmd)
        if result.returncode == 0 and result.stdout.strip():
            for item in json.loads(result.stdout):
                repos.add(item["repository"]["fullName"])

        print(f"  Searched: {pattern!r} — {len(repos)} repos so far", file=sys.stderr)
        time.sleep(SEARCH_RATE_LIMIT_PAUSE)

    return sorted(repos)


def clone_repo(repo_full_name: str, workdir: Path) -> Path:
    """Clone a repo into workdir. Returns the repo directory path."""
    repo_short = repo_full_name.split("/")[-1]
    dest = workdir / repo_short

    if dest.exists():
        print(f"  Already cloned: {repo_short}", file=sys.stderr)
        return dest

    result = gh_run(["repo", "clone", repo_full_name, str(dest), "--", "--depth", "1"])
    if result.returncode != 0:
        raise RuntimeError(f"Failed to clone {repo_full_name}: {result.stderr.strip()}")

    return dest


def create_branch(repo_dir: Path) -> None:
    """Create and checkout the migration branch."""
    result = git_run(["checkout", "-b", BRANCH_NAME], cwd=repo_dir)
    if result.returncode != 0:
        # Branch might already exist
        git_run(["checkout", BRANCH_NAME], cwd=repo_dir)


def scan_repo(repo_dir: Path, scanner_path: Path) -> dict:
    """Run the legacy scanner on a repo. Returns scan results."""
    result = subprocess.run(
        [sys.executable, str(scanner_path), str(repo_dir)],
        capture_output=True, text=True, timeout=120,
    )
    return {
        "exit_code": result.returncode,
        "output": result.stdout,
        "clean": result.returncode == 0,
    }


def get_changed_files(repo_dir: Path) -> list[str]:
    """Get list of files changed relative to the default branch."""
    # Get changed tracked files (staged + unstaged)
    staged = git_run(["diff", "--cached", "--name-only"], cwd=repo_dir)
    unstaged = git_run(["diff", "--name-only"], cwd=repo_dir)
    untracked = git_run(["ls-files", "--others", "--exclude-standard"], cwd=repo_dir)

    files: set[str] = set()
    for r in [staged, unstaged, untracked]:
        if r.returncode == 0:
            files.update(f.strip() for f in r.stdout.splitlines() if f.strip())
    return sorted(files)


def load_manifest(workdir: Path) -> dict:
    """Load the bulk migration manifest."""
    manifest_path = workdir / "manifest.json"
    if manifest_path.exists():
        return json.loads(manifest_path.read_text(encoding="utf-8"))
    return {"repos": {}}


def save_manifest(workdir: Path, manifest: dict) -> None:
    """Save the bulk migration manifest."""
    manifest_path = workdir / "manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2), encoding="utf-8")


