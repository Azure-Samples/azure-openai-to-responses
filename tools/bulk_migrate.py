# send-prs — create PRs for migrated repos
# ---------------------------------------------------------------------------

def cmd_send_prs(args: argparse.Namespace) -> int:
    """Create PRs for migrated repos. Supports --repos, --exclude, or interactive."""
    workdir = Path(args.workdir).resolve()
    manifest = load_manifest(workdir)

    if not manifest.get("repos"):
        print("No repos found. Run 'bulk prepare' first.", file=sys.stderr)
        return 1

    # Determine which repos to include
    include = set(args.repos.split(",")) if args.repos else None
    exclude = set(args.exclude.split(",")) if args.exclude else set()

    candidates = []
    for repo_short, info in manifest["repos"].items():
        if info.get("status") in ("error", "missing", "pr-sent"):
            continue
        if not info.get("path"):
            continue
        repo_dir = Path(info["path"])
        changed = get_changed_files(repo_dir)
        if not changed:
            continue
        if include and repo_short not in include:
            continue
        if repo_short in exclude:
            continue
        candidates.append((repo_short, info, changed))

    if not candidates:
        print("No repos ready for PRs.", file=sys.stderr)
        return 0

    # Show what we're about to do
    print(f"\n{'Repo':<40} {'Files changed':<15} {'Status'}")
    print("-" * 70)
    for repo_short, info, changed in candidates:
        print(f"  {repo_short:<38} {len(changed):<15} {info.get('status', '?')}")
    print()

    if not args.yes:
        answer = input(f"Send PRs for {len(candidates)} repo(s)? [y/N/one-by-one] ").strip().lower()
        if answer == "one-by-one":
            filtered = []
            for repo_short, info, changed in candidates:
                a = input(f"  Send PR for {repo_short} ({len(changed)} files)? [y/N] ").strip().lower()
                if a in ("y", "yes"):
                    filtered.append((repo_short, info, changed))
            candidates = filtered
        elif answer not in ("y", "yes"):
            print("Aborted.")
            return 0

    # Send PRs
    sent = 0
    failed = 0
    for repo_short, info, changed in candidates:
        repo_dir = Path(info["path"])
        repo_full = info["full_name"]
        print(f"\n--- {repo_full} ---")

        # Stage, commit, push
        git_run(["add", "-A"], cwd=repo_dir)
        commit_msg = "Migrate from Chat Completions to Responses API"
        git_run(["commit", "-m", commit_msg], cwd=repo_dir)

        push = git_run(["push", "-u", "origin", BRANCH_NAME], cwd=repo_dir)
        if push.returncode != 0:
            print(f"  Push failed: {push.stderr.strip()}")
            failed += 1
            continue

        # Create PR
        pr_result = gh_run(
            ["pr", "create",
             "--title", PR_TITLE,
             "--body", PR_BODY,
             "--head", BRANCH_NAME],
            cwd=repo_dir,
        )
        if pr_result.returncode == 0:
            pr_url = pr_result.stdout.strip()
            print(f"  PR created: {pr_url}")
            info["pr_url"] = pr_url
            info["status"] = "pr-sent"
            sent += 1
        else:
            print(f"  PR failed: {pr_result.stderr.strip()}")
            failed += 1

    save_manifest(workdir, manifest)

    print(f"\nDone: {sent} PR(s) sent, {failed} failed.")
    if sent > 0:
        print("\nAll PR URLs:")
        for _, info, _ in candidates:
            if info.get("pr_url"):
                print(f"  {info['pr_url']}")

    return 1 if failed > 0 else 0


