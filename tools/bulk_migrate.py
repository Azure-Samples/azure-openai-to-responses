def cmd_status(args: argparse.Namespace) -> int:
    """Show migration status with files changed per repo."""
    workdir = Path(args.workdir).resolve()
    manifest = load_manifest(workdir)

    if not manifest.get("repos"):
        print("No repos found. Run 'bulk prepare' first.", file=sys.stderr)
        return 1

    scanner = Path(__file__).resolve().parent.parent / ".github" / "skills" / "azure-openai-to-responses" / "scripts" / "detect_legacy.py"

    for repo_short, info in manifest["repos"].items():
        if info.get("status") == "error" or not info.get("path"):
            continue

        repo_dir = Path(info["path"])
        if not repo_dir.exists():
            info["status"] = "missing"
            continue

        # Re-scan to check if migration is done
        if scanner.exists():
            scan = scan_repo(repo_dir, scanner)
            info["scan_hits"] = not scan["clean"]

        # Check for changes
        changed = get_changed_files(repo_dir)
        info["files_changed"] = changed

        if info.get("pr_url"):
            info["status"] = "pr-sent"
        elif changed and not info.get("scan_hits"):
            info["status"] = "ready"
        elif changed:
            info["status"] = "in-progress"
        else:
            info["status"] = "scanned"

    save_manifest(workdir, manifest)
    print_report(manifest)
    return 0


