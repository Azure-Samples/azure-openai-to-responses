STATUS_LABELS = {
    "scanned": "Scanned (needs migration)",
    "in-progress": "In progress (has changes, still has legacy hits)",
    "ready": "Ready to PR (migrated, scanner clean)",
    "pr-sent": "PR sent",
    "error": "Error",
    "missing": "Directory missing",
}


def print_report(manifest: dict) -> None:
    """Print a formatted report of all repos."""
    repos = manifest.get("repos", {})
    if not repos:
        print("No repos in manifest.")
        return

    org = manifest.get("org", "?")
    print(f"\n{'='*72}")
    print(f"  Bulk Migration Report — {org}")
    print(f"{'='*72}\n")

    # Group by status
    by_status: dict[str, list] = {}
    for repo_short, info in repos.items():
        status = info.get("status", "unknown")
        by_status.setdefault(status, []).append((repo_short, info))

    # Summary
    print(f"  Total repos: {len(repos)}")
    for status, items in sorted(by_status.items()):
        label = STATUS_LABELS.get(status, status)
        print(f"    {label}: {len(items)}")
    print()

    # Detail table
    print(f"  {'Repo':<35} {'Status':<15} {'Files changed':<15} {'PR'}")
    print(f"  {'-'*35} {'-'*15} {'-'*15} {'-'*30}")

    for repo_short, info in sorted(repos.items()):
        status = info.get("status", "?")
        changed = info.get("files_changed", [])
        pr = info.get("pr_url") or "-"
        n_changed = str(len(changed)) if changed else "-"
        print(f"  {repo_short:<35} {status:<15} {n_changed:<15} {pr}")

    # Show files changed per repo (for repos that have changes)
    repos_with_changes = [(r, i) for r, i in repos.items() if i.get("files_changed")]
    if repos_with_changes:
        print(f"\n  --- Files changed per repo ---\n")
        for repo_short, info in sorted(repos_with_changes):
            print(f"  {repo_short}:")
            for f in info["files_changed"][:20]:
                print(f"    - {f}")
            if len(info["files_changed"]) > 20:
                print(f"    ... and {len(info['files_changed']) - 20} more")
            print()

    print(f"{'='*72}")


