def cmd_prepare(args: argparse.Namespace) -> int:
    """Clone flagged repos, create branches, scan, produce a consolidated report."""
    workdir = Path(args.workdir).resolve()
    workdir.mkdir(parents=True, exist_ok=True)

    scanner = Path(__file__).resolve().parent.parent / ".github" / "skills" / "azure-openai-to-responses" / "scripts" / "detect_legacy.py"
    if not scanner.exists():
        print(f"Error: scanner not found at {scanner}", file=sys.stderr)
        return 1

    # Verify gh auth
    auth = gh_run(["api", "/user", "--jq", ".login"])
    if auth.returncode != 0:
        print("Error: gh CLI not authenticated. Run 'gh auth login' first.", file=sys.stderr)
        return 1
    print(f"Authenticated as: {auth.stdout.strip()}", file=sys.stderr)

    # Discover repos
    print(f"\nDiscovering repos in '{args.org}' with legacy patterns...", file=sys.stderr)
    repos = discover_repos(args.org, args.language)
    if not repos:
        print("No repos with legacy patterns found.", file=sys.stderr)
        return 0

    print(f"\nFound {len(repos)} repos. Cloning and scanning...\n", file=sys.stderr)

    manifest = load_manifest(workdir)
    manifest["org"] = args.org
    manifest["language"] = args.language
    manifest["workdir"] = str(workdir)

    for repo_full in repos:
        repo_short = repo_full.split("/")[-1]
        print(f"--- {repo_full} ---", file=sys.stderr)

        try:
            repo_dir = clone_repo(repo_full, workdir)
            create_branch(repo_dir)
            scan = scan_repo(repo_dir, scanner)

            manifest["repos"][repo_short] = {
                "full_name": repo_full,
                "path": str(repo_dir),
                "scan_hits": not scan["clean"],
                "scan_output": scan["output"],
                "status": "scanned",
                "files_changed": [],
                "pr_url": None,
            }
        except Exception as e:
            print(f"  Error: {e}", file=sys.stderr)
            manifest["repos"][repo_short] = {
                "full_name": repo_full,
                "path": None,
                "scan_hits": None,
                "scan_output": str(e),
                "status": "error",
                "files_changed": [],
                "pr_url": None,
            }

    save_manifest(workdir, manifest)

    # Print report
    print_report(manifest)

    print(f"\nManifest saved: {workdir / 'manifest.json'}")
    print(f"\nNext steps:")
    print(f"  1. Migrate each repo (use the agent or SKILL.md)")
    print(f"  2. Check status:  python migrate.py bulk status --workdir {workdir}")
    print(f"  3. Send PRs:      python migrate.py bulk send-prs --workdir {workdir}")
    return 0


