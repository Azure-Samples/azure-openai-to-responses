def main() -> int:
    parser = argparse.ArgumentParser(
        prog="bulk-migrate",
        description="Bulk migration workflow: clone, scan, track, and send PRs across repos",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    # prepare
    p_prep = sub.add_parser("prepare", help="Clone repos, create branches, scan, produce report")
    p_prep.add_argument("--org", required=True, help="GitHub organization name")
    p_prep.add_argument("--language", help="Filter by language (e.g., python)")
    p_prep.add_argument("--workdir", default="./bulk-migrations", help="Working directory for cloned repos (default: ./bulk-migrations)")
    p_prep.set_defaults(func=cmd_prepare)

    # status
    p_status = sub.add_parser("status", help="Show migration status of all prepared repos")
    p_status.add_argument("--workdir", default="./bulk-migrations", help="Working directory")
    p_status.set_defaults(func=cmd_status)

    # send-prs
    p_pr = sub.add_parser("send-prs", help="Create PRs for migrated repos")
    p_pr.add_argument("--workdir", default="./bulk-migrations", help="Working directory")
    p_pr.add_argument("--repos", help="Comma-separated repo names to include (default: all ready)")
    p_pr.add_argument("--exclude", help="Comma-separated repo names to exclude")
    p_pr.add_argument("--yes", action="store_true", help="Skip confirmation prompt")
    p_pr.set_defaults(func=cmd_send_prs)

    args = parser.parse_args()
    return args.func(args)


if __name__ == "__main__":
    sys.exit(main())
